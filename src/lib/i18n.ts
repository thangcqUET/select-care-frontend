export type Locale = keyof typeof translations;
export type TranslationKey = keyof typeof translations['en'];

export function getTranslation(locale: Locale, key: TranslationKey): string | readonly string[] {
  const value = translations[locale]?.[key] || translations['en'][key];
  return value || key;
}

export function t(key: TranslationKey, locale: Locale = 'vi'): string | readonly string[] {
  return getTranslation(locale, key);
}

export function getCurrentLocale(pathname?: string): Locale {
  let path = pathname;
  
  if (!path && typeof window !== 'undefined') {
    path = window.location.pathname;
  }
  
  if (path) {
    const segments = path.split('/').filter(Boolean);
    if (segments[0] === 'en') return 'en';
    if (segments[0] === 'vi') return 'vi';
  }
  
  return 'vi'; // default
}

